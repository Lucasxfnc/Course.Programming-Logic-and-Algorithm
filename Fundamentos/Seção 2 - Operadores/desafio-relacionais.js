// Declarando variáveis:
let x = 1;
let y = 2;

// Encontre 5 expressões em que o resultado seja TRUE
console.log(x < y);
console.log(x <= y);
console.log(x != y);
console.log(x !== y);
console.log(x+x == y);

console.log("===============================================================");

// Encontre 5 expressões em que o resultado seja FALSE
