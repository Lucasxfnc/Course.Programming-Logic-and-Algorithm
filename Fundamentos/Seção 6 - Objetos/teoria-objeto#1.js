// A linguagem JavaScript é projetada com base em um simples paradigma orientado a objeto. Um objeto é uma coleção de propriedades, e uma propriedade é uma associação entre chave e um valor. Um valor de propriedade pode ser uma função, que é então considerada um método do objeto. 

// Objetos em JavaScript, assim como em muitas outras linguagens de programação, podem ser comparados com objetos na vida real. O conceito de objetos em JavaScript pode ser entendido com objetos tangíveis da vida real. 

// Em JavaScript, um objeto é uma entidade independente, com propriedades e tipos. Um objeto em JavaScript tem propriedades associadas a ele. Uma propriedade de um objeto pode ser explicada como uma variável que é ligada ao objeto. Propriedades de objetos são basicamente as mesmas que variáveis normais em JavaScript, exceto pelo fato de estarem ligadas a objetos. As propriedades de um objeto definem as características do objeto.

// chave : 'valor',
// Criando um objeto:
const myCars = {

    // Definindo seus atributos - Propriedades
    firstCar: {
        marca: 'Nissan',
        modelo: 'Kicks',
        versao: 'Exclusive',
        cor: 'Cinza Grafite',
    },
    secondCar: {
        marca: 'Peugeot',
        modelo: '3008',
        versao: 'Griffe',
        cor: 'Cinza Artanse',
    }
};
// Acessando seus atributos/propriedades e retornando na saída:
console.log('Primeira Opção: ', myCars.firstCar);
console.log('Segunda Opção: ', myCars.secondCar);


