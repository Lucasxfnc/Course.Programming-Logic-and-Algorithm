// typeof -> "tipos de" (dados/variáveis) O typeof é uma palavra-chave em JavaScript que retornará uma string indicando o tipo da variável/operando quando você a chama. Você pode usá-la para validar parâmetros de função ou verificar se as variáveis estão definidas. Há outros usos também. O operador typeof é útil porque é uma maneira fácil de verificar o tipo de uma variável em seu código.

let idade = 27;
console.log(typeof idade);

var salario = 2500.50;
console.log(typeof salario);

const bool = true // or false
console.log(typeof bool);

console.log(typeof "Fluminense");
console.log(typeof 'Manchester City');
