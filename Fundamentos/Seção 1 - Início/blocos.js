// Blocos de códigos (Introdução):
// Na programação de computadores, um bloco ou bloco de código é uma estrutura lexical de código-fonte que é agrupada. Os blocos consistem em uma ou mais declarações. 
// Se da através da abertura e fechamento de chaves '{}'
// Tem o objetivo de agrupar sentenças dentro de um bloco;
// Organização de um código(programa) pastas > arquivos > sentenças/blocos
{
    console.log("Bom Dia!!");
    console.log("Boa Tarde!!");
    console.log("Boa Noite!!");
}

{
    console.log("Passo #1");
    console.log("Passo #2");
    console.log("Passo #3");
}

{
    {
        {
            {
                {
                    {
                        ;
                    }
                }
            }
        }
    }   
}

console.log("Fim!");

